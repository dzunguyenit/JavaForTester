package array;

public class ForEachArray {
	public static void main(String args[]) {
		int[] myArray = { 1, 2, 5, 4, 0 };

		for (int bien : myArray) {
			System.out.println(bien);
		}

		// Print all the array elements
		for (int x : myArray) {
			System.out.println(x);
		}

	}
}
